CREATE TABLE EMPLOYEE (
	ID INT IDENTITY (1,1) PRIMARY KEY,
	USERNAME VARCHAR(20) UNIQUE NOT NULL,
	PASSWORD VARCHAR(150) NOT NULL,
	FULLNAME NVARCHAR(50) NOT NULL,
	ROLE VARCHAR(10) NOT NULL,
	GENDER BIT NOT NULL,
	EMAIL VARCHAR(50) UNIQUE NOT NULL,
	PHONE_NUMBER VARCHAR(20) UNIQUE NOT NULL,
	ADDRESS NVARCHAR(100),
	ACTIVATE BIT NOT NULL
)

CREATE TABLE BRAND (
	ID INT IDENTITY (1,1) PRIMARY KEY,
	NAME NVARCHAR(50) UNIQUE NOT NULL
)

CREATE TABLE PRODUCT (
	ID VARCHAR(20) PRIMARY KEY,
	NAME NVARCHAR(100) UNIQUE NOT NULL,
	DESCRIPTION NVARCHAR(100),
    ACTIVATE BIT NOT NULL,
	BRAND_ID INT NOT NULL,
	FOREIGN KEY (BRAND_ID) REFERENCES BRAND (ID)
)

CREATE TABLE PRODUCT_DETAIL (
	ID INT IDENTITY(1,1) PRIMARY KEY,
	SIZE FLOAT NOT NULL,
	PRICE MONEY NOT NULL,
	TYPE VARCHAR(20) NOT NULL,
    QUANTITY INT NOT NULL,
	IMPORT_DATE DATE NOT NULL,
	IMAGE NVARCHAR(50),
	BARCODE VARCHAR(48),
	PRODUCT_ID VARCHAR(20) NOT NULL,
	FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT (ID)
)

CREATE TABLE ORDERS (
	ID INT IDENTITY(1,1) PRIMARY KEY,
	CREATED_TIME DATETIME NOT NULL,
	EMPLOYEE_ID INT NOT NULL,
	FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE (ID)
)

CREATE TABLE ORDER_DETAIL (
	ID INT IDENTITY (1,1) PRIMARY KEY,
	QUANTITY INT NOT NULL,
	PRICE MONEY NOT NULL,
	PRODUCT_DETAIL_ID INT NOT NULL,
	ORDER_ID INT NOT NULL,
	FOREIGN KEY (PRODUCT_DETAIL_ID) REFERENCES PRODUCT_DETAIL (ID),
	FOREIGN KEY (ORDER_ID) REFERENCES ORDERS (ID)
)

CREATE TABLE BILL (
	ID INT IDENTITY(1,1) PRIMARY KEY,
	CREATED_TIME DATETIME NOT NULL,
	PAYMENT_METHOD VARCHAR(20) NOT NULL,
	EMPLOYEE_ID INT NOT NULL,
	ORDER_ID INT UNIQUE NOT NULL,
	FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE (ID),
	FOREIGN KEY (ORDER_ID) REFERENCES ORDERS (ID)
)

CREATE TABLE DELIVERY (
	ID INT IDENTITY (1,1) PRIMARY KEY,
	CUSTOMER_NAME NVARCHAR(50) NOT NULL,
	EMAIL VARCHAR(50),
	PHONE_NUMBER NVARCHAR(20) NOT NULL,
	ADDRESS NVARCHAR(100) NOT NULL,
	NOTE NVARCHAR(100),
	STATUS VARCHAR(20) NOT NULL,
	UPDATE_TIME DATETIME NOT NULL,
	ORDER_ID INT UNIQUE NOT NULL,
	FOREIGN KEY (ORDER_ID) REFERENCES ORDERS (ID)
)

CREATE TABLE EXCHANGE_PRODUCT (
	ID INT IDENTITY(1,1) PRIMARY KEY,
	CREATED_TIME DATETIME NOT NULL,
	NOTE NVARCHAR(120),
	O_PRODUCT_DETAIL_ID INT NOT NULL,
	N_PRODUCT_DETAIL_ID INT NOT NULL,
	BILL_ID INT UNIQUE NOT NULL,
	EMPLOYEE_ID INT NOT NULL,
	FOREIGN KEY (O_PRODUCT_DETAIL_ID) REFERENCES PRODUCT_DETAIL(ID),
	FOREIGN KEY (N_PRODUCT_DETAIL_ID) REFERENCES PRODUCT_DETAIL(ID),
	FOREIGN KEY (BILL_ID) REFERENCES BILL(ID),
	FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE(ID)
)


